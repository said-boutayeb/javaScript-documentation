//data type of string
let stg = "this is string"; //this is variable---------------------------------------: 1
console.log(typeof stg + ": 1");
//data type of number
let num = 309; //this is variable----------------------------------------------------: 2
console.log(typeof num + ": 2");
//data type of boolean
let boolOne = false; //this is variable----------------------------------------------: 3-1
let boolTwo = true; //this is variable-----------------------------------------------: 3-2
console.log(typeof boolOne + ": 3-1");
console.log(typeof boolTwo + ": 3-2");
//data type of Array not => object
let aryOne = [19, 29, 17, 28]; //this is variable------------------------------------: 4-1
let aryTwo = ["said", "simo", "fouad", "mohamed"]; //this is variable----------------: 4-2
console.log(typeof aryOne + "=> Array: 4-1");
console.log(typeof aryTwo + "=> Array: 4-2");
//data type of object
let objOne = { name: "said", age: 24, country: "Morocco", not: [28, 19, 8, "all", 92] }; //this is variable-------: 5-1
let objTwo = null; //this is variable------------------------------------------------: 5-2
console.log(typeof objOne + ": 5-1");
console.log(typeof objTwo + ": 5-2");
//data type of undefined
let und = undefined; //this is variable----------------------------------------------: 6
console.log(typeof und + ": 6");
